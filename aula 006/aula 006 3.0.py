#esse comando diz se é possivel converter um  valor em um tipo primitivo int antes dele
n = input('digite algo:')
print(n.isnumeric())

#diz se ele é alfabético
n = input('digite um algo:')
print(n.isalpha())

#diz se é alfabético e númerico
n = input('digite um algo:')
print(n.isalnum())

#aqui vai verificar se o valor está apenas com letras maiusculas
n = input('digite algo:')
print(n.isupper())
