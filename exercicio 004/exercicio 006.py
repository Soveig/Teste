v1 = input('digite algo: ')
print('tem espaços?', v1.isspace())
print('é um número?', v1.isnumeric())
print('é alfabético?', v1.isalpha())
print('é alfanumerico?', v1.isalnum())
print('está em maisculas?', v1.isupper())
print('está em minusculas?', v1.islower())
print('está capilizada?', v1.istitle())

