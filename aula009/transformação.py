frase = 'curso em video python'
#aqui ele vai trocar a palavra 'python' pela palavra 'android'
print(frase.replace('python', 'android'))
#aqui ele vai transformar todas as letras da string em maisculas
print(frase.upper())
#aqui ele vai transformar todas as letras da string em minusculas
print(frase.lower())
#ele deixa todos os caracteres em minusculas e apenas o primeiro caractere ele deixa maisculo
print(frase.capitalize())
#aqui ele vai contar quantas palavras existem na string e deixar os primeiros caracteres de cada palavra em maiusculo
print(frase.title())
#ele vai retirar os espaços no começo e no final da string
print(frase.strip())
#aqui ele vai retirar os espaços do lado direito da string
print(frase.rstrip())
#aqui ele vai retirar os espaços do lado esquerdo da string
print(frase.lstrip())