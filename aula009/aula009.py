frase = 'curso em video python'
#aqui ele vai indicar qual letra está na posição 9 da string
print(frase[9])
#no comando abaixo ele vai mostrar as letras da posição 9 até a posição 12
print(frase[9:13])
#aqui ele vai começar no 9 vai parar no 20, porém pulando as letras de 2 em 2
print(frase[9:21:2])
#aqui ele vai pegar as letras da posição 0 até a 5, ou seja, do inicio até o 5
print(frase[:5])
#aqui ele vai entender que é pra pegar as letras da posição 15 até o final
print(frase[15:])
#aqui quer dizer que vai mostrar do 9 até o final, porém pulando as letras de 3 em 3
print(frase[9::3])
